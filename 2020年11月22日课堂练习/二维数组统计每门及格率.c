#include <stdio.h>
#define N 5
int main()
{
int a[N][4];
int i,j,n1,n2,n3;
printf("请输入四组列表数据：学号，英语，数学，语文\n");
for(i=0;i<N;i++)
{
	for(j=0;j<4;j++)
	{
	   scanf("%d",&a[i][j]);
	}
}
n1=n2=n3=0; /*计数器清零*/
for(i=0;i<N;i++)  /*遍历数组行找到指定数组列参数小于某个条件的结果*/
{
	if(a[i][1]<60)  /*当前行的第一列参数小于60*/
	{
	  n1++;  /*当前计数器自增一*/
	}
	if(a[i][2]<60)
	{
	  n2++;
	}
	if(a[i][3]<60)
	{
	  n3++;
	}
}
/*开始输出各科及格成绩*/
printf("英语不及格：%4d\n数学不及格：%4d\n语文不及格：%4d\n",n1,n2,n3);
return 0;
}