#include <stdio.h>
int main(){
  char str[80],ch;  /*定义两个字符串变量。第一个为数组变量，数组长度80，第二个为单纯变量。*/
  int i,n=0;  /*设置循环计数器i，和内循环计数器n*/
  printf("请输入字符串：\n");
  scanf("%s",str);  /*接收字符串输入，使用%s控制类型，str是数组变量长度为80*/
  for(i=0;(ch=str[i])!='\0';i++)  /*循环条件，从0开始计数，条件2，当前输入字符串数组不等于字符串结束符\0*/
  {
	  if(ch>='A'&&ch<='Z'||ch>='a'&&ch<='z')  /*判断输入的字符串类型是大写或者小写英文字母*/
	  {
	    n++;  /*如果输入的是大写或小写字母，则计数器自增一。*/
	  }else{printf("您输入的不是大写或小写字母！\n");}
  }
  printf("您输入的字符串个数是:%d 个。\n",n);
return 0;
}