#include <stdio.h>
#define N 5
int main()
{
int a[N][7];
int i,j,n;
int max1,max2,max3;
printf("输入五行四列成绩：学号，数学，语文，英语\n");
for(i=0;i<N;i++)
{
	for(j=0;j<4;j++)
	{
	  scanf("%d",&a[i][j]);
	}
}
/*开始对成绩进行循环遍历处理数据*/
for(i=0;i<N;i++)
{
	for(a[i][4]=0,j=1;j<4;j++)
	{
	  a[i][4]+=a[i][j];
	}
	a[i][5]=a[i][4]/3;
	/*对比分数条件*/
	if(a[i][1]>=90&&a[i][2]>=90&&a[i][3]>=90)
	{
	  a[i][6]=1;
	}else
	{
	  a[i][6]=0;
	}
}
/*开始查询学号显示成绩*/
printf("请输入您要查询的学号：");
scanf("%d",&n);
/*for(i=0;i<N;i++)
{
	if(a[i][0]==n)
	{
	  a[i][4]+=a[i][j]
	}
}*/
i=0;
while(i<N&&a[i][0]!=n)
{
  i++; 
}
if(i<N)
{
  printf("该学生的信息，单科成绩，总成绩，平均分\n");
  for(j=1;j<6;j++)
  {
    printf("%4d",a[i][j]);
  }
  printf("\n");
}else
{
  printf("你要找的学号不存在！\n");
}
/*开始找出每门最高成绩*/
max1=a[0][1];max2=a[0][2];max3=a[0][3];
for(i=0;i<N;i++)
{
	if(a[i][1]>max1)
	{
	  max1=a[i][1];
	}
	if(a[i][2]>max2)
	{
	  max2=a[i][2];
	}
	if(a[i][3]>max3)
	{
	  max3=a[i][3];
	}
}
printf("输出最高分：数学:%4d,语文:%4d,英语：%4d\n",max1,max2,max3);
/*开始输出成绩表*/
printf("下面输出成绩单：\n");
printf("学号  数学  语文  英语  总成绩  平均分 优秀分\n");
printf("------------------------------------------------\n");
for(i=0;i<N;i++)
{
	for(j=0;j<7;j++)
	{
	  printf("%6d",a[i][j]);
	}
	printf("\n");
}
return 0;
}