#include <stdio.h>
int main(){
	int a[10]={78,89,54,64,67,58,63,90,26,93};  /*定义一个数组，长度为10*/
	int i,count=0;  /*定义计数器变量i，用于和数组下标对应，一般从0开始。count为满足条件后的计数器自增变量*/
	for(i=0;i<10;i++)  /*循环一维数组*/
	{
		if(a[i]>=85)  /*判断当前遍历数组的参数，如果有满足大于等于85的*/
		{
		  count++;  /*满足条件计数器count自增一，每满足一次自增一次，满足几次自增几次作为满足条件计数器使用。*/
		}
	}
	printf("满足条件的有：%-3d个学生。\n",count);
return 0;
}